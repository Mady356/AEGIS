@app.post("/api/encounter/begin")
async def api_encounter_begin(body: dict):
    """Create an encounter from a typed situation. The LLM produces the
    title, patient label, ordered procedural steps, and an initial brief;
    none of these come from the hardcoded ``scenarios`` module.

    Body: ``{situation: str}``. Returns the full encounter scaffold so
    the cockpit can render immediately without further round-trips.
    """
    situation = ((body or {}).get("situation") or "").strip()
    if not situation:
        raise HTTPException(400, "situation is required")

    try:
        intake_data, meta = await llm_agents.intake_to_encounter(situation)
    except inference.LLMError as exc:
        raise HTTPException(503, f"LLM unreachable: {exc}")

    # Create encounter under the LLM sentinel; persist situation + steps
    # as separate events so the audit chain stays append-only.
    enc = records.create_encounter(LLM_SCENARIO_ID, intake_data["patient_label"])
    enc_id = enc["id"]
    records.add_event(enc_id, "operator_situation_set", {"text": situation})
    records.add_event(enc_id, "encounter_steps_set", {
        "title": intake_data["title"],
        "steps": intake_data["steps"],
        "citations": intake_data.get("citations") or [],
    })

    # Compose the initial brief as a chat-ready string. Frontend prepends
    # this as the first assistant turn in the ASK AEGIS panel.
    brief = intake_data["brief"]
    brief_lines: list[str] = []
    summary = brief.get("summary", "").strip()
    head = f"ACUITY: {brief['acuity']}"
    if summary:
        head = f"{head} — {summary}"
    brief_lines.append(head)
    if brief.get("top_actions"):
        brief_lines.append("TOP ACTIONS: " + " ".join(
            f"{i+1}) {a}" for i, a in enumerate(brief["top_actions"])
        ))
    if brief.get("rule_outs"):
        brief_lines.append("RULE OUT: " + " ".join(
            f"{i+1}) {r}" for i, r in enumerate(brief["rule_outs"])
        ))
    brief_text = "\n".join(brief_lines)

    records.add_event(enc_id, "chat_turn", {
        "question": "(initial brief from situation intake)",
        "reply": brief_text[:1000],
        "citations": [],
        "latency_ms": meta.get("latency_ms"),
    })

    return {
        "encounter_id": enc_id,
        "id": enc_id,
        "scenario_id": LLM_SCENARIO_ID,
        "title": intake_data["title"],
        "patient_label": intake_data["patient_label"],
        "steps": intake_data["steps"],
        "citations": intake_data.get("citations") or [],
        "brief": {**brief, "text": brief_text},
        "latency_ms": meta.get("latency_ms"),
        "model": meta.get("model"),
    }


