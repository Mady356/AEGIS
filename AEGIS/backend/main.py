@app.post("/api/handoff/build")
async def api_handoff_build(body: dict):
    """V6 — Build the comprehensive single-PDF patient transfer document.
    No more zip wrapper, no separate .sig / .pub / verifier files —
    every clinically relevant fact about the encounter (situation,
    initial brief, procedural steps performed with completion times,
    citations with supporting quotes) is rendered in the PDF, with the
    integrity hash and Ed25519 signature printed in the document footer
    for chain-of-custody. Returns application/pdf."""
    enc_id = (body or {}).get("encounter_id", "")
    if records.get_encounter(enc_id) is None:
        raise HTTPException(404, "encounter not found")

    pdf_bytes, manifest = handoff.build_transfer_pdf(enc_id)
    headers = {
        "Content-Disposition": f'attachment; filename="{manifest["filename"]}"',
        "X-AEGIS-Bundle-Hash": manifest["integrity_hash"],
        "X-AEGIS-Signature": manifest["signature_hex"],
        "X-AEGIS-Pub-Fingerprint": manifest["device_pub_fingerprint"],
    }
    records.add_event(enc_id, "handoff_packet_built", {
        "filename": manifest["filename"],
        "size_bytes": manifest["size_bytes"],
        "integrity_hash": manifest["integrity_hash"],
    })
    return Response(content=pdf_bytes, media_type="application/pdf",
                    headers=headers)


