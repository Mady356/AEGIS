@app.post("/api/crisis")
async def api_crisis(body: dict) -> dict:
    """Run the offline orchestrator and return the one-screen response.

    Routes through the local LLM (LM Studio / Ollama) via llm_agents
    when reachable; falls back to deterministic stubs otherwise so the
    endpoint never breaks.

    Accepts either:
        {"responses": {...}, "scenario_id"?: str}
            — guided-form responses, normalized via intake
        {"encounter": {...}, "scenario_id"?: str}
            — already-structured encounter dict
        {..., "scenario_id"?: str}
            — treated as guided-form responses

    When scenario_id is present and known, scenario.primer_prompt /
    scenario.case / scenario.steps are merged into the encounter as
    `scenario_context` so the LLM bundle is scenario-aware. The
    Procedural Checklist on the frontend uses the bundle's `top_actions`
    (with per-item keywords for extraction matching) as its live source
    of truth, falling back to scenarios.steps when the LLM is offline.
    """
    body = body if isinstance(body, dict) else {}
    scenario_id = body.get("scenario_id")
    scenario = scenarios.get(scenario_id) if scenario_id else None

    if "responses" in body:
        encounter = intake.build_structured_encounter(
            body.get("responses") or {}, scenario=scenario
        )
    elif "encounter" in body:
        encounter = body.get("encounter") or {}
        if scenario and "scenario_context" not in encounter:
            encounter["scenario_context"] = {
                "id": scenario.get("id"),
                "case": scenario.get("case"),
                "primer_prompt": scenario.get("primer_prompt"),
                "steps": list(scenario.get("steps") or []),
            }
    else:
        encounter = intake.build_structured_encounter(body, scenario=scenario)

    try:
        out = await orchestrator.run_encounter_async(
            encounter,
            agents=llm_agents.LLM_AGENTS,
            offline_status=llm_agents.status_snapshot(),
        )
    except Exception as exc:                              # noqa: BLE001
        # Frontend uses {fallback: true} to switch to static checklist.
        return {
            "crisis_view": None,
            "fallback": True,
            "scenario_id": scenario_id,
            "error": str(exc),
            "offline_status": llm_agents.status_snapshot(),
        }
    if scenario_id:
        out["scenario_id"] = scenario_id
    return out


