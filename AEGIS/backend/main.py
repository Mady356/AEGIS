@app.get("/api/encounter/{enc_id}/procedural-steps")
async def api_proc_steps(enc_id: str):
    """Returns the step graph plus the current step rendering. The cockpit
    consumes this on first paint.

    For hardcoded scenarios, the graph comes from ``procedural_steps._GRAPHS``.
    For LLM-driven encounters (``scenario_id == LLM_SCENARIO_ID``) the graph
    comes from the latest ``encounter_steps_set`` event on the encounter."""
    rec = records.get_encounter(enc_id)
    if rec is None:
        raise HTTPException(404, "encounter not found")
    sid = rec["scenario_id"]
    graph = procedural_steps.graph_for(sid) or []
    if graph:
        current = procedural_steps.initial_step(sid)
        return {
            "scenario_id": sid,
            "current": current,
            "graph": [
                {
                    "id": s["id"],
                    "title": s["title"],
                    "icon": s.get("icon", "pulse"),
                    "instruction": s.get("instruction", ""),
                    "checklist_text": s["checklist_text"],
                    "why_matters": s.get("why_matters", ""),
                    "question": s.get("question"),
                }
                for s in graph
            ],
        }

    # LLM-driven path
    stored = records.get_encounter_steps(enc_id) or {}
    steps = stored.get("steps") or []
    if not steps:
        raise HTTPException(404, "no step graph for encounter")
    # V6 — return the FULL step shape on every graph entry so the
    # cockpit can navigate back to any previous step and re-render the
    # CURRENT STEP card with the original instruction, why_matters,
    # affirmation, icon, and citations. Previously only id/title/
    # checklist_text were sent, which made backward navigation render a
    # blank step body for LLM-driven encounters.
    return {
        "scenario_id": sid,
        "title": stored.get("title"),
        "citations": stored.get("citations") or [],
        "current": procedural_steps.initial_step_in(steps),
        "graph": [
            {
                "id": s["id"],
                "title": s["title"],
                "icon": s.get("icon", "pulse"),
                "instruction": s.get("instruction", ""),
                "checklist_text": s.get("checklist_text", s.get("title", "")),
                "why_matters": s.get("why_matters", ""),
                "affirmation": s.get("affirmation", ""),
                "question": s.get("question"),
            }
            for s in steps
        ],
    }


@app.post("/api/encounter/{enc_id}/advance-step")
async def api_advance_step(enc_id: str, body: dict):
    """Advance the procedural step. Body: {step_id, decision?}.
    Writes a step_completed event to the encrypted record."""
    rec = records.get_encounter(enc_id)
    if rec is None:
        raise HTTPException(404, "encounter not found")
    sid = rec["scenario_id"]
    step_id = body.get("step_id") or ""
    decision = body.get("decision")  # "yes" | "no" | None

    graph = procedural_steps.graph_for(sid)
    if graph:
        cur = procedural_steps.step_for(sid, step_id)
        if not cur:
            raise HTTPException(400, f"unknown step {step_id}")
        nxt = procedural_steps.advance(sid, step_id, decision)
    else:
        stored = records.get_encounter_steps(enc_id) or {}
        steps = stored.get("steps") or []
        cur = procedural_steps.step_in(steps, step_id)
        if not cur:
            raise HTTPException(400, f"unknown step {step_id}")
        nxt = procedural_steps.advance_in(steps, step_id, decision)

    try:
        records.add_event(enc_id, "step_completed", {
            "step_id": step_id,
            "title": cur["title"],
            "decision": decision,
            "next_step_id": (nxt or {}).get("id") if nxt and not nxt.get("complete") else None,
            "complete": bool(nxt and nxt.get("complete")),
        })
    except Exception:
        pass
    return {"ok": True, "next": nxt}


@app.get("/api/encounter/{enc_id}/context-log")
async def api_context_log(enc_id: str):
    """Chronological list of extracted clinical phrases for the left-column
    CONTEXT LOG. Synthesized from the encounter's events."""
    rec = records.get_encounter(enc_id)
    if rec is None:
        raise HTTPException(404, "encounter not found")
    sid = rec["scenario_id"]
    out: list[dict] = []
    # Seed for fresh encounter
    started_ms = (rec.get("events") or [{}])[0].get("t_offset_ms", 0)
    for s in procedural_steps.context_log_seed(sid):
        out.append({"t": s["t"], "text": s["text"], "kind": "seed"})

    def _fmt(ms: int) -> str:
        s = max(0, int(ms // 1000))
        return f"{s // 3600:02d}:{(s % 3600) // 60:02d}:{s % 60:02d}"

    for ev in rec.get("events") or []:
        et = ev.get("event_type")
        ts = _fmt(int(ev.get("t_offset_ms") or 0))
        p = ev.get("payload") or {}
        if et == "extraction_completed":
            txt = (p.get("summary") or {}).get("phrase") or p.get("phrase")
            if txt:
                out.append({"t": ts, "text": txt, "kind": "extracted"})
        elif et == "step_completed":
            out.append({"t": ts,
                        "text": f"Step complete: {p.get('title','')}",
                        "kind": "step"})
        elif et == "operator_phrase":
            out.append({"t": ts, "text": p.get("text", ""),
                        "kind": "extracted"})
        elif et == "operator_situation_set":
            txt = p.get("text", "")
            if txt:
                out.append({"t": ts, "text": f"SITUATION: {txt}",
                            "kind": "situation"})
    return {"entries": out}


