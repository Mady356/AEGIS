# V5 — Multi-turn chat (ASK AEGIS), situation-aware, RAG-grounded
# =====================================================================
_CHAT_SYSTEM_PROMPT = (
    "You are AEGIS, an offline clinical decision-support assistant for "
    "non-expert first responders in austere environments.\n"
    "Hard rules:\n"
    "  - Never make a diagnostic claim. Use 'consider', 'rule out', "
    "'treat as high risk until ruled out', 'escalate'.\n"
    "  - Be brief. 1-3 short sentences per reply unless the user asks "
    "for detail. Plain language. No jargon a medic wouldn't recognize.\n"
    "  - When supporting evidence is present in the [CORPUS] block, "
    "cite the chunk id in square brackets like [TCCC-TQ-PLACE]. If no "
    "chunk supports an answer, say so and suggest what the operator "
    "should ask or check next.\n"
    "  - The [SITUATION] and [CURRENT STEP] blocks are ground truth "
    "about the encounter. Use them. Do not contradict them.\n"
    "  - Never invent vitals, names, or events not present in the "
    "context.\n"
    "Briefing format (when the user asks for an encounter brief or "
    "to be briefed): respond with three short labeled lines, each on "
    "its own line, citing chunks where possible:\n"
    "  ACUITY: red | yellow | green — one short reason.\n"
    "  TOP ACTIONS: 1) ... 2) ... 3) ... [CHUNK]\n"
    "  RULE OUT: 1) ... 2) ... 3) ... [CHUNK]\n"
    "Otherwise reply conversationally per the rules above."
)


def _format_chat_user_prompt(question: str, situation: str,
                             current_step: dict | None,
                             chunks: list[dict]) -> str:
    parts: list[str] = []
    if situation:
        parts.append(f"[SITUATION]\n{situation.strip()}")
    if current_step and (current_step.get("title")
                         or current_step.get("instruction")):
        title = current_step.get("title", "").strip()
        instr = current_step.get("instruction", "").strip()
        parts.append(
            f"[CURRENT STEP]\n{title}"
            + (f"\n{instr}" if instr else "")
        )
    if chunks:
        parts.append("[CORPUS]\n" + inference._format_chunks(chunks))
    parts.append(f"[QUESTION]\n{question.strip()}")
    return "\n\n".join(parts)


@app.post("/api/chat")
async def api_chat(body: dict):
    """Multi-turn chat for the ASK AEGIS panel. Body:
        {encounter_id, scenario_id?, situation, history[], current_step?}
    history is a list of {role: 'user'|'assistant', content: str}.
    The latest user turn IS in history (last entry); we extract it
    for retrieval, then build a single user message that wraps the
    question with situation + current_step + RAG context.

    Returns {reply, citations[], latency_ms, model}.
    """
    enc_id = (body or {}).get("encounter_id") or ""
    scenario_id = (body or {}).get("scenario_id")
    situation = (body or {}).get("situation") or ""
    history = (body or {}).get("history") or []
    current_step = (body or {}).get("current_step")

    if not history or history[-1].get("role") != "user":
        raise HTTPException(400, "history must end with a user turn")
    question = (history[-1].get("content") or "").strip()
    if not question:
        raise HTTPException(400, "empty user question")

    # RAG grounding — k=3 keeps the prompt tight for fast turns.
    # For LLM-driven encounters (no fixed scenario), pass None so the
    # retrieval layer searches the full corpus rather than boosting
    # chunks tagged with a scenario the operator never picked.
    rag_filter = scenario_id if scenario_id and scenario_id != LLM_SCENARIO_ID \
        else None
    try:
        chunks = await retrieval.retrieve(question, rag_filter, k=3)
    except Exception:
        chunks = []

    user_prompt = _format_chat_user_prompt(
        question, situation, current_step, chunks,
    )

    # Build messages: system + prior history (excluding latest user)
    # + the wrapped latest user turn. Cap history to last 8 turns to
    # keep the prompt under Gemma 4's context budget at low latency.
    prior = [
        {"role": h["role"], "content": h.get("content", "")}
        for h in history[:-1] if h.get("role") in ("user", "assistant")
        and (h.get("content") or "").strip()
    ][-8:]
    messages = (
        [{"role": "system", "content": _CHAT_SYSTEM_PROMPT}]
        + prior
        + [{"role": "user", "content": user_prompt}]
    )

    try:
        reply, meta = await inference.chat_completion(messages)
    except inference.LLMError as exc:
        raise HTTPException(503, f"chat unreachable: {exc}")

    # Extract citation chip ids from [BRACKETS] in the reply text and
    # match against the retrieved chunk ids; surface only the chunks
    # the model actually cited so the UI doesn't overstate grounding.
    import re as _re
    cited_ids = set(_re.findall(r"\[([A-Z][A-Z0-9_-]{2,})\]", reply))
    citations = [
        {"citation_id": c.get("citation_id"),
         "supporting_quote": (c.get("text") or "")[:240]}
        for c in chunks if c.get("citation_id") in cited_ids
    ]

    if enc_id:
        try:
            records.add_event(enc_id, "chat_turn", {
                "question": question[:500],
                "reply": reply[:1000],
                "citations": [c["citation_id"] for c in citations],
                "latency_ms": meta.get("latency_ms"),
            })
        except Exception:
            pass

    return {
        "reply": reply,
        "citations": citations,
        "latency_ms": meta.get("latency_ms"),
        "model": meta.get("model"),
    }


