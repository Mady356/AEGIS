@app.get("/api/encounter/{enc_id}/situation")
async def api_situation_get(enc_id: str):
    """Return the most recent operator-set situation text for an
    encounter so the cockpit can prefill the SITUATION textarea on
    page load. Empty string if none has been set."""
    rec = records.get_encounter(enc_id)
    if rec is None:
        raise HTTPException(404, "encounter not found")
    latest_text = ""
    latest_t = 0
    for ev in rec.get("events") or []:
        if ev.get("event_type") != "operator_situation_set":
            continue
        t = int(ev.get("t_offset_ms") or 0)
        if t >= latest_t:
            latest_t = t
            latest_text = (ev.get("payload") or {}).get("text", "")
    return {"text": latest_text, "t_offset_ms": latest_t}


